package javadasar1;

public class OperasiPerbandingan {

    public static void main(String[] args) {

        int a = 100;
        int b = 100;
        
        System.err.println(a > b);
        System.err.println(a < b);
        System.err.println(a >= b);
        System.err.println(a <= b);
        System.err.println(a != b);

    }
}
